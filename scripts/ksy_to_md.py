#!/usr/bin/env python3
"""Generate docs/protocol/reference.md from protocol/fnirsi_dps150.ksy.

Usage:
    uv run python scripts/ksy_to_md.py \\
        --ksy protocol/fnirsi_dps150.ksy \\
        --out docs/protocol/reference.md
"""

from __future__ import annotations

import argparse
import textwrap
from pathlib import Path
from typing import Any

import yaml  # type: ignore[import-untyped]

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _ks_type_label(t: Any) -> str:  # noqa: ANN401
    """Human-readable label for a KS field type."""
    if isinstance(t, str):
        mapping = {
            "u1": "u8",
            "u2": "u16 LE",
            "u4": "u32 LE",
            "f4": "f32 LE",
            "f8": "f64 LE",
            "s1": "i8",
            "s2": "i16 LE",
            "s4": "i32 LE",
            "str": "ASCII string",
        }
        return mapping.get(t, t)
    if isinstance(t, dict):
        if "switch-on" in t:
            return "payload (switch)"
    return str(t)


def _md_table(headers: list[str], rows: list[list[str]]) -> str:
    cols = len(headers)
    widths = [max(len(h), max((len(r[i]) for r in rows), default=0)) for i, h in enumerate(headers)]
    sep = "| " + " | ".join("-" * w for w, _ in zip(widths, headers, strict=True)) + " |"
    head = "| " + " | ".join(h.ljust(w) for h, w in zip(headers, widths, strict=True)) + " |"
    body = "\n".join(
        "| " + " | ".join(str(r[i]).ljust(widths[i]) for i in range(cols)) + " |" for r in rows
    )
    return f"{head}\n{sep}\n{body}"


# ---------------------------------------------------------------------------
# Section renderers
# ---------------------------------------------------------------------------


def render_header(ksy: dict[str, Any]) -> str:
    meta = ksy.get("meta", {})
    doc = textwrap.dedent(str(meta.get("doc", ""))).strip()
    rows = [
        ["ID", str(meta.get("id", ""))],
        ["Title", str(meta.get("title", ""))],
        ["KS version", str(meta.get("ks-version", ""))],
        ["Endian", str(meta.get("endian", ""))],
        ["License", str(meta.get("license", ""))],
    ]
    table = _md_table(["Property", "Value"], rows)
    wire = ""
    wire_prefixes = ("[DIR]", "DIR", "START", "CHKSUM", "Values")
    for line in doc.splitlines():
        if "Wire format" in line or any(line.strip().startswith(p) for p in wire_prefixes):
            wire += line + "\n"
    sections = [
        "<!-- AUTO-GENERATED by scripts/ksy_to_md.py — do not edit manually -->",
        "",
        "# Protocol Reference",
        "",
        "Single source of truth: [`protocol/fnirsi_dps150.ksy`](../../protocol/fnirsi_dps150.ksy)",
        "",
        "## Overview",
        "",
        table,
        "",
    ]
    if wire.strip():
        sections += [
            "## Wire Format",
            "",
            "```",
            wire.strip(),
            "```",
            "",
        ]
    return "\n".join(sections)


def render_enums(ksy: dict[str, Any]) -> str:
    enums: dict[str, dict[int, str]] = ksy.get("enums", {})
    if not enums:
        return ""
    out = ["## Enumerations", ""]
    for name, values in enums.items():
        out.append(f"### `{name}`")
        out.append("")
        rows = [[f"`0x{v:02X}`", k] for v, k in sorted(values.items())]
        out.append(_md_table(["Value (hex)", "Name"], rows))
        out.append("")
    return "\n".join(out)


def render_commands(ksy: dict[str, Any]) -> str:
    """Build a command catalogue from the command_id enum + payload switch cases."""
    enums: dict[str, Any] = ksy.get("enums", {})
    cmd_enum: dict[int, str] = enums.get("command_id", {})
    types: dict[str, Any] = ksy.get("types", {})

    # Extract payload switch cases from frame type
    frame_seq: list[dict[str, Any]] = types.get("frame", {}).get("seq", [])
    cases: dict[str, str] = {}
    for field in frame_seq:
        if field.get("id") == "payload":
            t = field.get("type", {})
            if isinstance(t, dict):
                cases = t.get("cases", {})
            break

    # Map ksy enum key (e.g. "command_id::push_output") → payload type name
    case_map: dict[str, str] = {}
    for case_key, payload_type in cases.items():
        short = case_key.split("::")[-1] if "::" in case_key else case_key
        case_map[short] = payload_type

    out = ["## Command Catalogue", ""]
    rows = []
    for value, name in sorted(cmd_enum.items()):
        payload_type = case_map.get(name, "—")
        payload_doc = ""
        if payload_type and payload_type != "—":
            payload_doc = types.get(payload_type, {}).get("doc", "")
            payload_doc = textwrap.shorten(str(payload_doc).replace("\n", " "), width=60)
        rows.append([f"`0x{value:02X}`", f"`{name}`", f"`{payload_type}`", payload_doc])
    out.append(_md_table(["Hex", "Name", "Payload type", "Description"], rows))
    out.append("")
    return "\n".join(out)


def render_types(ksy: dict[str, Any]) -> str:
    types: dict[str, Any] = ksy.get("types", {})
    out = ["## Payload Types", ""]
    for type_name, typedef in types.items():
        if type_name == "frame":
            continue
        doc = textwrap.dedent(str(typedef.get("doc", ""))).strip()
        seq: list[dict[str, Any]] = typedef.get("seq", [])
        out.append(f"### `{type_name}`")
        out.append("")
        if doc:
            # Only first line as a brief description
            out.append(doc.splitlines()[0])
            out.append("")
        if seq:
            rows = []
            for field in seq:
                fname = str(field.get("id", ""))
                ftype = _ks_type_label(field.get("type", ""))
                fdoc = str(field.get("doc", "")).replace("\n", " ").strip()
                repeat = field.get("repeat")
                repeat_expr = field.get("repeat-expr")
                if repeat == "expr" and repeat_expr:
                    ftype += f" ×{repeat_expr}"
                if field.get("size-eos"):
                    ftype = "bytes (rest)"
                fenum = field.get("enum")
                if fenum:
                    ftype += f" (enum: `{fenum}`)"
                rows.append([f"`{fname}`", ftype, fdoc])
            out.append(_md_table(["Field", "Type", "Description"], rows))
            out.append("")
        else:
            out.append("_(no fields)_")
            out.append("")
    return "\n".join(out)


def render_checksum() -> str:
    return "\n".join(
        [
            "## Checksum",
            "",
            "Every frame ends with a 1-byte checksum:",
            "",
            "```",
            "CHKSUM = (CMD + LEN + Σ DATA bytes) mod 256",
            "```",
            "",
            "The `DIR` and `START` bytes are **excluded** from the checksum calculation.",
            "Confirmed by byte-exact comparison against captured frames.",
            "",
        ]
    )


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    p.add_argument("--ksy", required=True, type=Path, help="Path to .ksy file")
    p.add_argument("--out", required=True, type=Path, help="Output Markdown file")
    return p.parse_args()


def load_ksy(path: Path) -> dict[str, Any]:
    with path.open() as f:
        return yaml.safe_load(f)  # type: ignore[no-any-return]


def main() -> None:
    args = parse_args()
    ksy = load_ksy(args.ksy)

    sections = [
        render_header(ksy),
        render_enums(ksy),
        render_commands(ksy),
        render_types(ksy),
        render_checksum(),
    ]
    content = "\n".join(sections)

    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(content)
    print(f"Written: {args.out}")


if __name__ == "__main__":
    main()
